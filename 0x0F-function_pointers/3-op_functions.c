#include "3-calc.h"
/**
 *op_add - add two ints
 *@a: int 1
 *@b: int 2
 *Return: result
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 *op_sub - substract two ints
 *@a: int 1
 *@b: int 2
 *Return: result
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 *op_mul - multiplicate two ints
 *@a: int 1
 *@b: int 2
 *Return: result
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 *op_div - divide two ints
 *@a: int 1
 *@b: int 2
 *Return: result
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 *op_mod - modulo between two ints
 *@a: int 1
 *@b: int 2
 *Return: result
 */
int op_mod(int a, int b)
{
	return (a % b);
}
